    DefaultTableModel tabelaModel = new DefaultTableModel(new Object[]{"ID", "NOME", "EMAIL", "PASSWORD", "ROLE"}, 0);

    private void setTable() {
        tabelaUsuario.setCellSelectionEnabled(true);
        tabelaUsuario = new JTable(tabelaModel);
        jScrollPane1.setViewportView(tabelaUsuario);
    }

    private UsuarioDaoImpl usuarioList = new UsuarioDaoImpl();

    private void selectUsuario() {
        HibernateConfig hc = new HibernateConfig();
        tabelaModel.setRowCount(0);
        usuarioList.seletUsuario(hc.getEntityManager()).forEach(usuario -> {
            tabelaModel.addRow(new Object[]{
                usuario.getId(),
                usuario.getNome(),
                usuario.getEmail(),
                usuario.getPassword(),
                usuario.getRole().getRoles(),});
        });
    }
